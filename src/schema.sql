
/* Drop Tables */

DROP TABLE IF EXISTS PERSON_ADDRESS;
DROP TABLE IF EXISTS PERSON;




/* Create Tables */

CREATE TABLE PERSON
(
	ID INT NOT NULL,
	NAME VARCHAR DEFAULT 'empty',
	PRIMARY KEY (ID)
) WITHOUT OIDS;


CREATE TABLE PERSON_ADDRESS
(
	ID BIGINT NOT NULL,
	ADDRESS VARCHAR(0),
	PERSON_ID INT NOT NULL,
	PRIMARY KEY (ID)
) WITHOUT OIDS;



/* Create Foreign Keys */

ALTER TABLE PERSON_ADDRESS
	ADD FOREIGN KEY (PERSON_ID)
	REFERENCES PERSON (ID)
	ON UPDATE RESTRICT
	ON DELETE RESTRICT
;



